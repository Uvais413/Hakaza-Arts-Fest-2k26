// SEARCH LOGIC
const searchInput = document.getElementById('chestInput');
const searchBtn = document.getElementById('searchBtn');
const resultDisplay = document.getElementById('resultDisplay');

function findResult() {
    const query = searchInput.value.trim();
    resultDisplay.innerHTML = ""; 

    if (!query) {
        resultDisplay.innerHTML = `<p class="placeholder-text">Please enter a number</p>`;
        return;
    }

    const result = db.find(item => item.view === query);

    if (result) {
        resultDisplay.innerHTML = `
            <div class="res-card" style="background:#fff; padding:20px; border-radius:10px; border:2px solid gold; margin-top:20px; animation: slideUp 0.3s ease;">
                <h3>Chest No: ${result.view}</h3>
                <p style="color:green; font-weight:bold; margin:10px 0;">Result Available!</p>
                <div style="display:flex; gap:10px; justify-content:center;">
                    <a href="${result.dl}" target="_blank" style="padding:10px 20px; background:#eee; color:#004d40; text-decoration:none; border-radius:5px;"><i class="fa fa-eye"></i> View</a>
                </div>
            </div>
        `;
    } else {
        resultDisplay.innerHTML = `<p style="color:red; font-weight:bold;">No result found for Chest No ${query}</p>`;
    }
}

searchBtn.addEventListener('click', findResult);
searchInput.addEventListener('keypress', (e) => {
    if (e.key === 'Enter') findResult();
});

